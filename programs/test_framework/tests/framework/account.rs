pub fn get_file_account() -> Keypair {
  let config_file = CONFIG_FILE.as_ref()
    .ok_or_else(|| panic!("unable to get config file path"))
    .unwrap();
  let config = Config::load(&config_file)
    .unwrap();
  read_keypair_file(&config.keypair_path).unwrap()
}

